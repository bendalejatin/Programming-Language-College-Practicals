#Practical : 3- Demonstrate the use of basic string methods in python -   lower(), islower(), 
#upper(), isupper(), join(), split(), find(), replace(), capitalize(), casefold(), and replace().

#string
s='Abc'

#lower()
print(s.lower())

#islower()
print(s.islower())

#upper()
print(s.upper())

#isupper()
print(s.isupper())

#join()
print("#".join("python"))

#split()
x='continue'
print(x.split('n'))

#find()
y="Hello, welcome to amtics."
print(y.find("amtics"))

#replace()
a="I like apple."
print(a)
print(a.replace('like','love'))

#capitalize()
z="aBc"
print(z.capitalize())

#casefold()
p="Hello,And Welcome TO Amtics."
print(p.casefold())
